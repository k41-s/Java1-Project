package hr.algebra.model;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlAttribute;

/**
 *
 * @author kaish
 * 
 * CREATE TABLE Organiser (
    IDOrganiser INT IDENTITY PRIMARY KEY,
    Name NVARCHAR(300),
    Surname NVARCHAR(300),
    Email NVARCHAR(20),
    Phone NVARCHAR(20)
    );
 */

@XmlAccessorType(XmlAccessType.FIELD)
public class Organiser implements Comparable<Organiser>{
    
    private int id;
    private String name;
    private String surname;
    private String email;
    private String phone;

    public Organiser() {
    }

    public Organiser(String name, String surname, String email, String phone) {
        this.name = name;
        this.surname = surname;
        this.email = email;
        this.phone = phone;
    }

    public Organiser(int id, String name, String surname, String email, String phone) {
        this.id = id;
        this.name = name;
        this.surname = surname;
        this.email = email;
        this.phone = phone;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSurname() {
        return surname;
    }

    public void setSurname(String surname) {
        this.surname = surname;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    @Override
    public String toString() {
        return id + ": " + name + ' ' + surname;
    }

    @Override
    public int hashCode() {
        int hash = 7;
        hash = 59 * hash + this.id;
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Organiser other = (Organiser) obj;
        return this.id == other.id;
    }

    @Override
    public int compareTo(Organiser o) {
        return Integer.compare(this.id, o.getId());
    }
}
